export function getRandomWordPT(usedIds: string[] = [], difficulty?: Difficulty | "All"): WordEntry | null {
  let pool = WORSHIP_WORDS_PT;
  if (difficulty && difficulty !== "All") {
    pool = pool.filter((w) => w.difficulty === difficulty);
  }
  const unused = pool.filter((w) => !usedIds.includes(w.id));
  if (unused.length === 0) {
    if (pool.length === 0) return null;
    return pool[Math.floor(Math.random() * pool.length)];
  }
  return unused[Math.floor(Math.random() * unused.length)];
}
